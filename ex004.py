n=input('Digite algo: ')
print('O tipo primitivo desse valor e', type(n))
print('E numerico?', n.isnumeric())
print('E alfabetico??', n.isalpha())
print('Tem letra minuscula??', n.islower())
print('Tem letra maiscula??', n.isupper())
print('E alfanumerico??', n.isalnum())
print('So tem espaço??', n.isspace())
print('Esta capitalizada??', n.istitle())







